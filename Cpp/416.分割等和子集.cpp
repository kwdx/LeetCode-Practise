class Solution {
public:
    bool canPartition(vector<int>& nums) {
        if (nums.size() < 2) return false;
        int sum = accumulate(nums.begin(), nums.end(), 0);

        int target = sum >> 1;
        /// 总和为奇数，不能平等分割
        if (sum & 0x1) return false;

        vector<bool> dp(target + 1);
        dp[0] = true;
        
        for (int num : nums) {
            for (int i = target; i >= num; i--) {
                dp[i] = dp[i] || dp[i - num];
            }
        }
        return dp[target];
    }
};
